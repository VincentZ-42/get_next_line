#include "libft.h"

size_t				ft_int_len(int n)
{
	unsigned int	nbr;
	size_t			len;

	nbr = (n < 0) ? n * -1 : n;
	len = (n <= 0) ? 1 : 0;
	while (nbr > 0)
	{
		nbr /= 10;
		len++;
	}
	return (len);
}
